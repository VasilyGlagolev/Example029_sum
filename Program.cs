// Задача 60. Сформируйте трёхмерный массив из неповторяющихся двузначных чисел. 
// Напишите программу, которая будет построчно выводить массив, добавляя индексы каждого элемента.
// Массив размером 2 x 2 x 2
// 66(0,0,0) 25(0,1,0)
// 34(1,0,0) 41(1,1,0)
// 27(0,0,1) 90(0,1,1)
// 26(1,0,1) 55(1,1,1)

class Program
{
    static void Main()
    {
        int[,,] array = new int[2, 2, 2]; // создание трехмерного массива

        // заполнение массива пользователем
        for (int i = 0; i < 2; i++)
        {
            for (int j = 0; j < 2; j++)
            {
                for (int k = 0; k < 2; k++)
                {
                    Console.WriteLine("Введите значение для элемента массива [{0},{1},{2}]:", i, j, k);
                    if (int.TryParse(Console.ReadLine(), out int value))
                    {
                        array[i, j, k] = value; // добавление числа в массив
                    }
                    else
                    {
                        Console.WriteLine("Ошибка ввода! Введите целое число.");
                        k--; // чтобы повторить ввод для этого же элемента
                    }
                }
            }
        }

        // вывод массива с индексами каждого элемента
        for (int i = 0; i < 2; i++)
        {
            for (int j = 0; j < 2; j++)
            {
                for (int k = 0; k < 2; k++)
                {
                    Console.Write($"{array[i, j, k]}({i},{j},{k})\t");
                }
                Console.WriteLine();
            }
            Console.WriteLine();
        }
    }
}
